package day20;

import java.util.Scanner;

public class ForLoopCount {

	public static void main(String[] args) {

		
		
		for (int i = 10; i > 0; i--) {

			System.out.print(i + " ");

		}
		System.out.println();

		for (int i = 1; i < 11; i++) {

			System.out.print(i + " ");
		}
		
		

	}
}