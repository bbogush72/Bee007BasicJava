package loops;

public class WhileLoop3 {

	public static void main(String[] args) {
	
		/*
		 ( ) --> boolean literal : true/false
		 boolean variables : boolean b =true;
		 boolean expression : x<5;
		 */
		int x=0;		
		while(x<5) {
			++x;
			
			System.out.println("Life is short!");
			
		}
		
	}

}
