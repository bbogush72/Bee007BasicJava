package Loops;

public class Nested_Loop {

	public static void main(String[] args) {

		int count =4;
		
		
		//while(count<i)
		
		
		
		for (int i = 15; i >4; i--) {
			System.out.print(i + " ");
			count++;
		}
		System.out.println();
		for (int i = 15; i >4; i--) {
			System.out.print(i + " ");
			count++;
		}
		System.out.println();
		for (int i = 15; i >4; i--) {
			System.out.print(i + " ");
			count++;
		}
		System.out.println();
		for (int i = 15; i >4; i--) {
			System.out.print(i + " ");
			count++;
		}
		System.out.println();
	
	
	
}

}

