package Loops;

public class nested2 {

	public static void main(String[] args) {

		for (int j = 0; j < 4; j++) {

			  System.out.println("Iteration " + (j+1) );
			  for(int i=15 ; i>=5 ; i--) {
			    System.out.print(i + " ");
			  }
			  System.out.println();
			  
			}

	}

}
